// Funciones auxiliares para filtrar y buscar
export const getActiveMembers = (): TeamMember[] => {
  return teamMembers.filter((member) => member.activo)
}

export const getMembersByArea = (area: string): TeamMember[] => {
  return teamMembers.filter((member) => member.area === area && member.activo)
}

export const getMemberById = (id: string): TeamMember | undefined => {
  return teamMembers.find((member) => member.id === id)
}

export const getMembersByGender = (genero: "masculino" | "femenino"): TeamMember[] => {
  return teamMembers.filter((member) => member.genero === genero && member.activo)
}

export const getJefaturaPrincipal = (): TeamMember[] => {
  return teamMembers.filter(
    (member) =>
      (member.cargo.includes("Comandante") ||
        member.cargo.includes("Primer Jefe") ||
        member.cargo.includes("Segundo Jefe") ||
        member.cargo.includes("Jefe de Administración") ||
        member.cargo.includes("Jefe de Servicios") ||
        member.cargo.includes("Jefe de Operaciones") ||
        member.cargo.includes("Jefe de Sanidad") ||
        member.cargo.includes("Jefe de Imagen")) &&
      member.activo,
  )
}

export const getTotalMembers = (): number => {
  return teamMembers.filter((member) => member.activo).length
}

export const getTotalMembersByArea = (area: string): number => {
  return teamMembers.filter((member) => member.area === area && member.activo).length
}
